package member;

public class AuthLevelDTO {
	private String idx;
	private String ismng;
	private String name;
	private int level;
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getIsmng() {
		return ismng;
	}
	public void setIsmng(String ismng) {
		this.ismng = ismng;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
}
